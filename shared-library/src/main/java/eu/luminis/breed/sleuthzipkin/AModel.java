package eu.luminis.breed.sleuthzipkin;

public class AModel {

  private String a;
  private String b;
  private String c;
  private String d;

  public AModel() {
  }

  public AModel(String a, String b, String c, String d) {
    this.a = a;
    this.b = b;
    this.c = c;
    this.d = d;
  }

  public String getA() {
    return a;
  }

  public String getB() {
    return b;
  }

  public String getC() {
    return c;
  }

  public String getD() {
    return d;
  }

  public void setA(String a) {
    this.a = a;
  }

  public void setB(String b) {
    this.b = b;
  }

  public void setC(String c) {
    this.c = c;
  }

  public void setD(String d) {
    this.d = d;
  }
}
